"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const tabs = [
  { href: "/conversation", label: "Converse", icon: ConversationIcon },
  { href: "/reflections", label: "Reflections", icon: ReflectionIcon },
  { href: "/gift", label: "Gift", icon: GiftIcon },
  { href: "/connections", label: "Connections", icon: ConnectionsIcon },
];

export function BottomNav() {
  const pathname = usePathname();

  // Hide nav on auth pages
  if (pathname?.startsWith("/auth")) return null;

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white/80 backdrop-blur-md border-t border-curious-100 safe-area-pb z-50">
      <div className="flex items-center justify-around h-14 max-w-lg mx-auto">
        {tabs.map((tab) => {
          const isActive =
            pathname === tab.href || pathname?.startsWith(tab.href + "/");
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`flex flex-col items-center gap-0.5 px-4 py-1 transition-colors ${
                isActive ? "text-curious-800" : "text-curious-400"
              }`}
            >
              <tab.icon active={isActive} />
              <span className="text-[10px] font-medium">{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

